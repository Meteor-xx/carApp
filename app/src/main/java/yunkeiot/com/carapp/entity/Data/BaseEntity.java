package yunkeiot.com.carapp.entity.Data;

public class BaseEntity {
    private String resCode;
    private String version;
    private String resSrt;
    private String resMsg;

    public boolean isSuccesses() {
        if ("1000".equals(resCode)) {
            return true;
        }
        return false;
    }

    public String getCode() {
        return resCode;
    }

    public void setCode(String resCode) {
        this.resCode = resCode;
    }

    public String getVersion() {
        return version;
    }

    public void setVersion(String version) {
        this.version = version;
    }

    public String getResSrt() {
        return resSrt;
    }

    public void setResSrt(String resSrt) {
        this.resSrt = resSrt;
    }

    public String getResMsg() {
        return resMsg;
    }

    public void setResMsg(String resMsg) {
        this.resMsg = resMsg;
    }

}
