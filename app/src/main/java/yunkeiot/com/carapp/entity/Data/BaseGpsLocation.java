package yunkeiot.com.carapp.entity.Data;

public class BaseGpsLocation {
    private float gpsSpeed;
    private double latitude;
    private double longitude;
    private float height;
    private float heading;
    private float latitudeType;
    private float longitudeType;
    private float satellitesNumber;
    private long gpsTime;
    private String addressText;
    public double getLatitude() {
        return latitude;
    }

    public void setLatitude(double latitude) {
        this.latitude = latitude;
    }

    public double getLongitude() {
        return longitude;
    }

    public void setLongitude(double longitude) {
        this.longitude = longitude;
    }

    public long getGpsTime() {
        return gpsTime;
    }

    public void setGpsTime(long gpsTime) {
        this.gpsTime = gpsTime;
    }

    public String getAddressText() {
        return addressText;
    }

    public void setAddressText(String addressText) {
        this.addressText = addressText;
    }

    public float getGpsSpeed() {
        return gpsSpeed;
    }

    public void setGpsSpeed(float gpsSpeed) {
        this.gpsSpeed = gpsSpeed;
    }

    public float getHeight() {
        return height;
    }

    public void setHeight(float height) {
        this.height = height;
    }

    public float getHeading() {
        return heading;
    }

    public void setHeading(float heading) {
        this.heading = heading;
    }

    public float getLatitudeType() {
        return latitudeType;
    }

    public void setLatitudeType(float latitudeType) {
        this.latitudeType = latitudeType;
    }

    public float getLongitudeType() {
        return longitudeType;
    }

    public void setLongitudeType(float longitudeType) {
        this.longitudeType = longitudeType;
    }

    public float getSatellitesNumber() {
        return satellitesNumber;
    }

    public void setSatellitesNumber(float satellitesNumber) {
        this.satellitesNumber = satellitesNumber;
    }
}
